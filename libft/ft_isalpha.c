#include "libft.h"

/*
ASCII 
65 = 'A'; 90 = 'Z';
97 = 'a'; 122 = 'z';
*/
int	ft_isalpha(int c)
{
	if (c >= 65 && c <= 90)
		return (1);
	else if (c >= 97 && c <= 122)
		return (2);
	return (0);
}
